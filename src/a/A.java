package a;

public class A {
	private int a = 20;
	int b = 60;
	protected int c = 40;
	public int d = 10;

	public static void main(String[] args) {
		A ob = new A();
		System.out.println(ob.a);
		System.out.println(ob.b);
		System.out.println(ob.c);
		System.out.println(ob.d);
	}

}
