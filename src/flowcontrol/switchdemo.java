package flowcontrol;

public class switchdemo {

	public static void main(String[] args) {
		int x = 2;
		switch (x) {
		case 1:
			System.out.println("happy monday");
			break;
		case 2:
			System.out.println("happy tuesday");
			break;
		case 3:
			System.out.println("happy wednesday");
			break;
		}

	}

}
